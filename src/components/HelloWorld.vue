          <p>非同质化代币（NFT, Non-fungible token）指的是一类具有唯一性的数字资产，这些资产的所有权是在链上（比如以太坊区块链上）流转的。从数字商品（如存在于虚拟世界中的物品）到物理资产的债权（如服装或房地产）都可以用 NFT 表示。在未来几年，我们将看到 NFT 在一些全新的应用场景中使用，而它们都只有在区块链上才能实现。尽管目前大多数 NFT 项目都活跃在以太坊生态中，但是其他智能合约平台同样可以支持 NFT。毕竟，归根到底，NFT 只是表示独一无二的资产的数字化抽象物。“非同质化代币”，这个词听起来不太直观，因为在物理世界里，我们通常不会在意物体的同质性，但是当考虑如何在区块链上表示资产时，同质与否会产生重要的技术区别。这篇文章并不打算详细介绍 NFT 的方方面面，而是站在一个较高的视角概述 NFT 是什么，为什么它们很有意思，并介绍他们潜在的一些应用。</p>
